#include "libft.h"

long long int	ft_atoi(const char *str)
{
	int				minus;
	int				i;
	long long int	sum;

	i = 0;
	minus = 1;
	sum = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		minus = -minus;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		sum = sum * 10 + str[i] - '0';
		i++;
	}
	return (minus * sum);
}
