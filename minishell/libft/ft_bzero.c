#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*s2;

	if (n == 0)
		return ;
	i = 0;
	s2 = (char *)s;
	while (i != (int)n)
	{
		s2[i] = '\0';
		i++;
	}
}
